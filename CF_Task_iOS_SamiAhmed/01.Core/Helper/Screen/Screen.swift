import SwiftUI

enum Screen {
    static let width = UIScreen.main.bounds.width
    static let height = UIScreen.main.bounds.height

    static let halfWidth = width / 2
    static let halfHeight = height / 2

    static let threeQuarterWidth = width * 0.75
    static let threeQuarterHeight = height * 0.75

    static let quarterWidth = width * 0.25
    static let quarterHeight = height * 0.25
}
